package com.fb.vo;

public class Lookup {

	private String lookupType;
	private String lookupCde;
	private String lookupNme;
	private Boolean displayFlag;
	private Integer displayOrder;
	private String dscr;
	
	public String getLookupType() {
		return lookupType;
	}
	public void setLookupType(String lookupType) {
		this.lookupType = lookupType;
	}
	public String getLookupCde() {
		return lookupCde;
	}
	public void setLookupCde(String lookupCde) {
		this.lookupCde = lookupCde;
	}
	public String getLookupNme() {
		return lookupNme;
	}
	public void setLookupNme(String lookupNme) {
		this.lookupNme = lookupNme;
	}
	public Boolean getDisplayFlag() {
		return displayFlag;
	}
	public void setDisplayFlag(Boolean displayFlag) {
		this.displayFlag = displayFlag;
	}
	public Integer getDisplayOrder() {
		return displayOrder;
	}
	public void setDisplayOrder(Integer displayOrder) {
		this.displayOrder = displayOrder;
	}
	public String getDscr() {
		return dscr;
	}
	public void setDscr(String dscr) {
		this.dscr = dscr;
	}
	

}
